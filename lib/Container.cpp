#include <Zen/Fabric/Container.hpp>

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Container::Container()
:   m_shouldStop(false)
,   m_reactor()
{

}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
Container::~Container()
{

}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Container::registerConnection(Zen::Enterprise::I_Connection& _connection)
{
    // TODO If Container needs to retain a reference to _connection then
    // the _connection should be passed as a shared_ptr<I_Connection> instead
    // of a reference.
    _connection.registerReactor(m_reactor);
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Container::startProcessing()
{
    m_workerThread = std::thread(
        [this]
        {
            while(!m_shouldStop)
            {
                m_reactor.poll(100);
            }
        }
    );
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Container::stopProcessing()
{
    m_shouldStop = true;
}

//-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~-~
void
Container::waitForJoin()
{
    m_workerThread.join();
}

